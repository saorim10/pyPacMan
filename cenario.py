    def pintar_linha(self, tela, n_linha, linha):
        for n_coluna, coluna in enumerate(linha):
            x = n_coluna * self.tamanho
            y = n_linha * self.tamanho
            cor = PRETO
            if coluna == 2:
                cor = AZUL_ESCURO
            pygame.draw.rect(tela, cor, (x, y, self.tamanho, self.tamanho), 0)
            if coluna == 1:
                pygame.draw.circle(tela, AMARELO, (x + self.tamanho // 2, y + self.tamanho // 2), self.tamanho // 10, 0)

            if coluna == 0:
                cor = PRETO

    def pintar(self, tela):
        for n_linha, linha in enumerate(self.matriz):
            self.pintar_linha(tela, n_linha, linha)

    def calc_regras(self):
        col = self.pac.coluna_intencao
        lin = self.pac.linha_intencao
        if 0 <= col <= 27 and 0 <= lin <= 28:
            if self.matriz[lin][col] != 2:
                self.pac.permitir_movimento()
                if self.matriz[lin][col] == 1:
                    self.pontos += 1
                    self.matriz[lin][col] = 0
